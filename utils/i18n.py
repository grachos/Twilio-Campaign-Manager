class LanguageManager:
    _instance: Optional["LanguageManager"] = None

    def __new__(cls) -> "LanguageManager":
        if cls._instance is None:
            cls._instance = super().__new__(cls)
            cls._instance._initialized = False
        return cls._instance

    def __init__(self) -> None:
        if self._initialized:
            return
        self._initialized = True
        self._lang: str = "es"
        self._callbacks: list = []

    @property
    def lang(self) -> str:
        return self._lang

    @lang.setter
    def lang(self, value: str) -> None:
        if value in ("en", "es") and value != self._lang:
            self._lang = value
            for cb in self._callbacks:
                try:
                    cb(self._lang)
                except Exception:
                    pass

    def register_callback(self, callback) -> None:
        if callback not in self._callbacks:
            self._callbacks.append(callback)

    def unregister_callback(self, callback) -> None:
        if callback in self._callbacks:
            self._callbacks.remove(callback)

    def t(self, key: str) -> str:
        return TRANSLATIONS.get(self._lang, TRANSLATIONS["en"]).get(key, TRANSLATIONS["en"].get(key, key))


_lang_manager = LanguageManager()


def tr(key: str) -> str:
    return _lang_manager.t(key)
